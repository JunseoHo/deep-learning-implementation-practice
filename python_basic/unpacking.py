"""

언패킹이란, 여러 개의 값을 가지는 데이터 구조 (리스트, 튜플 등)에서 각 원소를 개별 변수에 할당하는 기능이다.

간단하게 아래 예시를 살펴보자.

"""

lst = ["This is a", 'This is b', 'This is c']

a, b, c = lst
print (a, b, c)


"""

만약 2차원 이상의 리스트라면 리스트가 변수에 할당된다.

"""

lst = [[1, 2, 3], [10, 20, 30], [100, 200, 300]]

a, b, c = lst
print(a, b, c)


"""

단, 원소의 개수가 할당하고자 하는 변수의 개수와 맞지 않으면 에러가 발생한다.

"""

# lst = [1, 2, 3]
# a, b = lst  # ValueError: too many values to unpack (expected 2)


"""

* 연산자는 패킹/언패킹에서는 가변 인자를 뜻한다.
단, 패킹/언패킹 연산 한번에 * 연산자는 단 한번만 사용할 수 있다는 점에 유의하라.

"""

lst = [1, 2, 3, 4, 5]
first, *rest = lst  # *rest는 남은 모든 값을 리스트로 패킹
print(first, rest)  # 1, [2, 3, 4, 5]